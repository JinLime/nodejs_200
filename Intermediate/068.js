// Math.sinh() 함수는 sinh함수를 구하는 함수
// Math.cosh() 함수는 cosh함수를 구하는 함수
// Math.tanh() 함수는 tanh함수를 구하는 함수
const sinh1 = Math.sinh(0); // 0
const sinh2 = Math.sinh(1); // 1.175201193
const sinh3 = Math.sinh(-1); // -1.1752011936

const cosh1 = Math.cosh(0); // 1
const cosh2 = Math.cosh(1); // 1.5430806348152
const cosh3 = Math.cosh(-1); // 1.5430806348152

const tanh1 = Math.tanh(0); // 0
const tanh2 = Math.tanh(Infinity); // 1
