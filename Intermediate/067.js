// Math.asin() 함수는 arc sin 함수를 구함
// Math.acos() 함수는 arc cos 함수를 구함
// Math.atan() 함수는 arc tan 함수를 구함
const asin1 = Math.asin(0); // 0
const asin2 = Math.asin(1); // 1.5707963267
const asin3 = Math.asin(Math.PI / 2); // NaN

const acos1 = Math.acos(0); // 1.5707963267
const acos2 = Math.acos(1); // 0
const acos3 = Math.acos(-1.2); // NaN

const atan1 = Math.atan(Math.PI / 2); // 1.003884821
