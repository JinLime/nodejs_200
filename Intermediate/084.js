const numbers = [3, 6, 2, 8, 1];
const strings = ["abc", "bcd", "cdv", "rrr", "qews"];

const sortedNumbers = numbers.sort();
const sortedStrings = strings.sort();

console.log("sortedNumbers :", sortedNumbers);
console.log("sortedStrings :", sortedStrings);

const reverseSortedNumbers = sortedNumbers.reverse();
const reverseSortedStrings = sortedStrings.reverse();

console.log("reverseSortedNumbers :", reverseSortedNumbers);
console.log("reverseSortedStrings :", reverseSortedStrings);
